--[[ Init ]]--
for shopId, shop in pairs(Shops) do
	if shop.Blip then
		shop.Blip.Name = shop.Name
	end

	for locationId, location in ipairs(shop.Locations) do
		-- Register entity.
		exports.entities:Register({
			id = shopId.."-"..locationId,
			name = shop.Name,
			coords = location.Coords,
			radius = 3.0,
			customization = {
				location = locationId,
				shop = shopId,
			},
			navigation = {
				text = shop.Name,
				icon = "checkroom",
				sub = shopId == "clothing" and Outfits.navigation,
			},
		})

		-- Create blip.
		if location.Blip and shop.Blip then
			local blip = AddBlipForCoord(location.Coords.x, location.Coords.y, location.Coords.z)
			SetBlipInfo(blip, shop.Blip)
		end
	end
end

--[[ Events ]]--
AddEventHandler("entities:onInteract", function(object)
	local target = object.customization
	if not target then return end

	local shop = Shops[target.shop]
	if not shop then return end

	local location = shop.Locations[target.location]
	if not location then return end

	local coords = location.Coords
	if not coords then return end

	local ped = PlayerPedId()

	SetEntityCoordsNoOffset(ped, coords.x, coords.y, coords.z)
	SetEntityHeading(ped, coords.w)

	Editor:Toggle(true, target.shop)
end)